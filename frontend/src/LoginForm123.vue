          registerForm.password.length < 6 ? '–ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è –¥–ª–∏–Ω–∞ –ø–∞—Ä–æ–ª—è - 6 —Å–∏–º–≤–æ–ª–æ–≤' : ''

        errors.confirmPassword = !registerForm.confirmPassword ? '–ü–æ–¥—Ç–≤–µ—Ä–¥–∏—Ç–µ –ø–∞—Ä–æ–ª—å' :
          registerForm.password !== registerForm.confirmPassword ? '–ü–∞—Ä–æ–ª–∏ –Ω–µ —Å–æ–≤–ø–∞–¥–∞—é—Ç' : ''
      } else {
        errors.email = !loginForm.email ? 'Email –æ–±—è–∑–∞—Ç–µ–ª–µ–Ω' : 
          !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(loginForm.email) ? '–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π email' : ''
        
        errors.password = !loginForm.password ? '–ü–∞—Ä–æ–ª—å –æ–±—è–∑–∞—Ç–µ–ª–µ–Ω' : 
          loginForm.password.length < 6 ? '–ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è –¥–ª–∏–Ω–∞ –ø–∞—Ä–æ–ª—è - 6 —Å–∏–º–≤–æ–ª–æ–≤' : ''
      }

      return !Object.values(errors).some(error => error)
    }

    const handleSubmit = async () => {
      if (!validateForm()) return

      try {
        loading.value = true
        error.value = null
        
        if (isRegister.value) {
          await authStore.register({
            username: registerForm.username,
            email: registerForm.email,
            password: registerForm.password
          })
        } else {
          await authStore.login({
            email: loginForm.email,
            password: loginForm.password,
            remember: loginForm.remember
          })
        }

        router.push({ name: 'contests' })
      } catch (err) {
