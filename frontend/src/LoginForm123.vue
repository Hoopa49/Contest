          // –û—à–∏–±–∫–∏ –æ—Ç —Å–µ—Ä–≤–µ—Ä–∞ (–µ—Å—Ç—å –æ—Ç–≤–µ—Ç)
          const { status, data } = err.response
          
          switch (status) {
            case 400:
              if (data?.error?.code === 'VALIDATION_ERROR') {
                error.value = data.error.message || '–ù–µ–≤–µ—Ä–Ω—ã–π email –∏–ª–∏ –ø–∞—Ä–æ–ª—å'
              } else {
                error.value = data?.error?.message || '–û—à–∏–±–∫–∞ –≤–∞–ª–∏–¥–∞—Ü–∏–∏ –¥–∞–Ω–Ω—ã—Ö'
              }
              break
            case 401:
              error.value = '–ù–µ–≤–µ—Ä–Ω—ã–π email –∏–ª–∏ –ø–∞—Ä–æ–ª—å'
              break
            case 404:
              error.value = '–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω'
              break
            case 429:
              error.value = '–°–ª–∏—à–∫–æ–º –º–Ω–æ–≥–æ –ø–æ–ø—ã—Ç–æ–∫ –≤—Ö–æ–¥–∞. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–¥–æ–∂–¥–∏—Ç–µ'
              break
            case 500:
              error.value = '–í–Ω—É—Ç—Ä–µ–Ω–Ω—è—è –æ—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ'
              break
            default:
              error.value = data?.error?.message || '–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏'
          }
        } else if (err.request) {
          // –û—à–∏–±–∫–∏ —Å–µ—Ç–∏ (–Ω–µ—Ç –æ—Ç–≤–µ—Ç–∞)
          error.value = '–°–µ—Ä–≤–µ—Ä –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –∫ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç—É'
        } else {
          // –î—Ä—É–≥–∏–µ –æ—à–∏–±–∫–∏
          error.value = '–ü—Ä–æ–∏–∑–æ—à–ª–∞ –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –æ—à–∏–±–∫–∞'
        }
      } finally {
        loading.value = false
      }
    }

    const handleSocialAuth = async (provider) => {
      try {
        loading.value = true
        error.value = null
        await authStore.socialLogin(provider)
      } catch (err) {
        error.value = err.message || '–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ —á–µ—Ä–µ–∑ —Å–æ—Ü–∏–∞–ª—å–Ω—É—é —Å–µ—Ç—å'
        console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ —á–µ—Ä–µ–∑ —Å–æ—Ü—Å–µ—Ç—å:', {
          message: err.message,
          response: err.response?.data,
          stack: err.stack
        })
        // –ù–µ –¥–µ–ª–∞–µ–º —Ä–µ–¥–∏—Ä–µ–∫—Ç –ø—Ä–∏ –æ—à–∏–±–∫–µ
        return
      } finally {
