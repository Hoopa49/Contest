        errors.email = !registerForm.email ? 'Email –æ–±—è–∑–∞—Ç–µ–ª–µ–Ω' : 
          !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(registerForm.email) ? '–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π email' : ''
        
